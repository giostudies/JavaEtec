package fabricacalcado.model.com;

public class Calcado {
	private String nomeCalcado;
	private int tamanhoCalcado;
	
	public String getNomeCalcado() {
		return nomeCalcado;
	}
	public void setNomeCalcado(String nomeCalcado) {
		this.nomeCalcado = nomeCalcado;
	}
	public int getTamanhoCalcado() {
		return tamanhoCalcado;
	}
	public void setTamanhoCalcado(int tamanhoCalcado) {
		this.tamanhoCalcado = tamanhoCalcado;
	}
	
	

}
